def _parse_initials(raw):
    """Read the optional `[initials]` table from `raw`.

    Returns a `dict` mapping a (lowercased) field name to a `dict`
    that maps a full field value (or `@string` macro name) to the
    initials the `%c` format specifier should produce for it."""
    table = raw.get("initials", {})
    if not isinstance(table, dict):
        raise ValueError("[initials] must be a table")
    initials = {}
    for field, mapping in table.items():
        if not isinstance(mapping, dict) or not all(
            isinstance(key, str) and isinstance(value, str)
            for key, value in mapping.items()
        ):
            raise ValueError(
                f"[initials.{field}] must be a table mapping field "
                "values to strings"
            )
        initials[field.lower()] = dict(mapping)
    return initials


def _parse_journal_macros(raw):
    """Read the optional `[journal_macros]` table from `raw`.

    The table maps an `@string` macro name to the full journal name it
    stands for, or to a list of names (the first being the canonical
    value of the macro, the rest aliases that resolve to the same
    macro on import). Returns a `dict` mapping the normalized
    (lowercased) macro name to a tuple of names."""
    table = raw.get("journal_macros", {})
    if not isinstance(table, dict):
        raise ValueError("[journal_macros] must be a table")
    journal_macros = {}
    for name, value in table.items():
        if not is_valid_macro_name(name, normalized=False):
            raise ValueError(
                f"[journal_macros] contains an invalid macro name: {name!r}"
            )
        names = [value] if isinstance(value, str) else value
        if (
            not isinstance(names, list)
            or not names
            or not all(
                isinstance(journal, str) and journal.strip()
                for journal in names
            )
        ):
            raise ValueError(
                f"[journal_macros] {name} must be a journal name or a "
                "non-empty list of journal names"
            )
        normalized = normalize_macro_name(name)
        if normalized in journal_macros:
            raise ValueError(
                f"[journal_macros] defines macro {normalized!r} more "
                "than once"
            )
        journal_macros[normalized] = tuple(
            journal.strip() for journal in names
        )
    return journal_macros


def _parse_protected_words(raw):
    """Read the optional `protected_words` key from `raw`.

    Returns a list of words (or phrases) that
    {meth}`~bibdeskparser.Library.import_bibtex` always wraps in
    braces inside a `title` to protect their capitalization."""
    value = raw.get("protected_words", [])
    if not isinstance(value, list) or not all(
        isinstance(word, str) and word.strip() for word in value
    ):
        raise ValueError("protected_words must be a list of non-empty strings")
    return [word.strip() for word in value]


# -- the active configuration ----------------------------------------- #


class Config:
    """The process-global configuration, publicly exposed as the
    `Library.config` class attribute (and thus also readable from any
    library instance, `bib.config`).

    The single instance of this class is {data}`active`; {meth}`load`
    and {meth}`reset` mutate it in place, so every reference to it stays
    current. Attributes may be assigned directly for an in-process
    override; assignment never writes back to the configuration file,
    and (re)loading a configuration replaces any override.

    Attributes (see the [configuration](configuration) reference page
    for their meaning and TOML spelling):

    * `verify_types` (default `True`): whether an unrecognized entry
      type is rejected with a `ValueError`.
    * `verify_fields` (default `True`): whether assigning a field
      inappropriate for an entry's type emits a `UserWarning`.
    * `config_file` (default `None`): an explicit `bibdeskparser.toml`
      path that takes precedence over directory-based discovery.
    * `default_bib_file` (default `None`): the `.bib` file the
      command-line interface operates on when none is given.
    * `auto_key` (an {class}`AutoKey`): the `[auto_key]` settings for
      autogenerated citation keys -- `auto_key.format_spec` (the format
      for {meth}`~bibdeskparser.Library.rekey`), `auto_key.lowercase`,
      and `auto_key.clean`.
    * `auto_file` (an {class}`AutoFile`): the `[auto_file]` settings
      for autogenerated attachment file names (used by
      {meth}`~bibdeskparser.Library.add_file` and
      {meth}`~bibdeskparser.Library.rename_file`) --
      `auto_file.format_spec`, `auto_file.location`,
      `auto_file.lowercase`, `auto_file.clean`, and
      `auto_file.file_automatically`.
    * `add_abstract` (an {class}`AddAbstract`): the `[add_abstract]`
      defaults for the keyword arguments of
      {meth}`~bibdeskparser.Library.add_abstract` --
      `add_abstract.min_confidence` (default `"high"`) and
      `add_abstract.mark_empty` (default `False`).
    * `add_preprint` (an {class}`AddPreprint`): the `[add_preprint]`
      defaults for the keyword arguments of
      {meth}`~bibdeskparser.Library.add_preprint` --
      `add_preprint.mark_empty` (default `False`).
    * `initials` (default `{}`): per-field acronym exceptions for the
      `%c` format specifier.
    * `journal_macros` (default `{}`): journal-name-to-macro mappings
      used by {meth}`~bibdeskparser.Library.import_bibtex` (and thus
      {meth}`~bibdeskparser.Library.add`) to resolve a journal name to
      an `@string` macro -- a `dict` mapping a macro name to a tuple
      of journal names (the first being the canonical value of the
      macro, the rest aliases).
    * `protected_words` (default `[]`): words (or phrases) that
      {meth}`~bibdeskparser.Library.import_bibtex` always wraps in
      braces inside a `title` to protect their capitalization.

    The remaining attributes hold the effective entry-type/field data
    model -- the built-in tables from `bibdeskparser.entrytypes`,
    possibly extended or replaced by a config file: `documented_types`,
    `recognized_entry_types`, `universal_fields`, `known_fields`, and
    the derived `type_fields` (the per-type union of required and
    optional fields, recomputed from `documented_types` on load).
    """

    # `reset` (not `__init__` directly) assigns all instance
    # attributes, so that `__init__` and a later `reset()` call share
    # the same logic.
    # pylint: disable=attribute-defined-outside-init

    def __init__(self):
        self.reset()

    def reset(self):
        """Reset all configuration to the built-in defaults, including
        the `config_file` override. Mainly useful for tests."""
        self.config_file = None
        self._apply_defaults()

    def _apply_defaults(self):
        """Reset every *loadable* setting (everything except the
        `config_file` override) to the built-in defaults."""
        self.verify_types = True
        self.verify_fields = True
        self.default_bib_file = None
        self.auto_key = AutoKey()
        self.auto_file = AutoFile()
        self.add_abstract = AddAbstract()
        self.add_preprint = AddPreprint()
        self.initials = {}
        self.journal_macros = {}
        self.protected_words = []
        self.documented_types = {
            entry_type: {
                "required": tuple(spec["required"]),
                "optional": tuple(spec["optional"]),
            }
            for entry_type, spec in entrytypes.DOCUMENTED_TYPES.items()
        }
        self.recognized_entry_types = set(entrytypes.RECOGNIZED_ENTRY_TYPES)
        self.universal_fields = set(entrytypes.UNIVERSAL_FIELDS)
        self.known_fields = set(entrytypes.KNOWN_FIELDS)
        self._recompute()

    def _recompute(self):
        """Derive `type_fields` from `documented_types`."""
        self.type_fields = {
            entry_type: frozenset(spec["required"])
            | frozenset(spec["optional"])
            for entry_type, spec in self.documented_types.items()
        }

    def load(self, bib_dir=None):
        """Discover and apply a `bibdeskparser.toml`.

        Searches (see {func}`discover`, honoring the `config_file`
        override) and, if a file is found, parses it and applies it in
        place; otherwise resets every loadable setting to the built-in
        defaults (`config_file` itself is kept). Returns the `Path`
        that was applied, or `None` if none was found.

        Raises {exc}`ValueError` if the discovered file is malformed
        (in which case the configuration is left unchanged), and
        {exc}`FileNotFoundError` if an explicit `config_file` does not
        exist.
        """
        path = discover(bib_dir=bib_dir, config_file=self.config_file)
        if path is None:
            self._apply_defaults()
            return None
        # Parse and validate everything *before* mutating any attribute,
        # so a malformed file leaves the configuration unchanged.
        raw = _parse(path)
        default_bib_file = _parse_default_bib_file(raw)
        auto_key = _parse_auto_key(raw)
        auto_file = _parse_auto_file(raw)
        add_abstract = _parse_add_abstract(raw)
        add_preprint = _parse_add_preprint(raw)
        initials = _parse_initials(raw)
        journal_macros = _parse_journal_macros(raw)
        protected_words = _parse_protected_words(raw)
        tables = _build(raw)
        self.verify_types = tables["verify_types"]
        self.verify_fields = tables["verify_fields"]
        self.documented_types = tables["documented_types"]
        self.recognized_entry_types = tables["recognized_entry_types"]
        self.universal_fields = tables["universal_fields"]
        self.known_fields = tables["known_fields"]
        self._recompute()
        self.default_bib_file = default_bib_file
        self.auto_key = auto_key
        self.auto_file = auto_file
        self.add_abstract = add_abstract
        self.add_preprint = add_preprint
        self.initials = initials
        self.journal_macros = journal_macros
        self.protected_words = protected_words
        return path

    # -- validation against the active data model --------------------- #

    def normalize_entry_type(self, value):
        """Return the normalized (lowercased) form of an entry type.

        `value` (a `str`) is lowercased and validated against the
        recognized entry types (the types BibDesk templates plus the
        extended data-model types, as configured). If entry-type
        validation is disabled (`verify_types`), any lowercased value
        is accepted.

        Raises {exc}`TypeError` if `value` is not a `str`, and
        {exc}`ValueError` if it is not a recognized entry type.

        ```python
        >>> from bibdeskparser.config import active
        >>> active.normalize_entry_type("Article")
        'article'
        >>> active.normalize_entry_type("bogus")
        Traceback (most recent call last):
            ...
        ValueError: invalid entry type: 'bogus'

        ```
        """
        if not isinstance(value, str):
            raise TypeError(f"entry type must be a str, not {type(value)!r}")
        lowered = value.lower()
        if not self.verify_types:
            return lowered
        if lowered in self.recognized_entry_types:
            return lowered
        raise ValueError(f"invalid entry type: {value!r}")

    def is_known_field(self, key):
        """Return whether `key` is a recognized field name.

        `key` is compared case-insensitively against the recognized
        field names (the classic BibTeX/BibDesk fields together with
        the extended data-model fields, plus any added through the
        configuration file).

        ```python
        >>> from bibdeskparser.config import active
        >>> active.is_known_field("Title")
        True
        >>> active.is_known_field("nonsense")
        False

        ```
        """
        return key.lower() in self.known_fields

    def field_is_appropriate(self, entry_type, key):
        """Return whether `key` is an appropriate field for
        `entry_type`.

        Both arguments are compared case-insensitively. If field
        validation is disabled (`verify_fields`), every field is
        treated as appropriate. Otherwise a field is appropriate if it
        is a universal field (accepted on any type), or -- for a type
        BibDesk templates -- one of that type's required/optional
        fields. For a recognized type that BibDesk does not template,
        any recognized field is appropriate.

        ```python
        >>> from bibdeskparser.config import active
        >>> active.field_is_appropriate("article", "Journal")
        True
        >>> active.field_is_appropriate("article", "publisher")
        False
        >>> active.field_is_appropriate("book", "keywords")  # universal
        True

        ```
        """
        if not self.verify_fields:
            return True
        lkey = key.lower()
        if lkey in self.universal_fields:
            return True
        allowed = self.type_fields.get(entry_type.lower())
        if allowed is not None:
            return lkey in allowed
        return self.is_known_field(lkey)


