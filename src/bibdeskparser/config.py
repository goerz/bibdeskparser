"""The active configuration, and loading of `bibdeskparser.toml`.

This module defines the {class}`Config` class and its single instance
{data}`active`, the process-global configuration: the
`verify_types`/`verify_fields` validation flags, the effective
entry-type/field tables (the built-in data model from
`bibdeskparser.entrytypes`, possibly extended or replaced by a config
file), the `auto_key` settings for autogenerated citation keys
(used by {meth}`~bibdeskparser.Library.rekey`), and the `auto_file`
settings for autogenerated attachment file names (used by
{meth}`~bibdeskparser.Library.add_file` and
{meth}`~bibdeskparser.Library.rename_file`). The configuration is
process-global because entry-type and field validation happens on
individual {class}`~bibdeskparser.Entry` objects, which hold no
reference to any owning library.

{data}`active` is exposed publicly as the `Library.config` class
attribute. A `bibdeskparser.toml` file replicates some of the
preferences the BibDesk application offers; see the
[configuration](configuration) reference page for the file format and
discovery rules.
"""

import os
import warnings
from pathlib import Path

from . import entrytypes, specifiers
from .macros import is_valid_macro_name, normalize_macro_name

