#: The name of the configuration file that is searched for.
CONFIG_FILENAME = "bibdeskparser.toml"

#: The environment variable naming the user-level configuration file,
#: replacing the XDG location.
CONFIG_ENV_VAR = "BIBDESKPARSER_CONFIG"

# The recognized top-level keys of a `bibdeskparser.toml`; anything else
# triggers a (non-fatal) warning, for forward compatibility.
_KNOWN_TOP_LEVEL_KEYS = frozenset(
    (
        "verify_types",
        "verify_fields",
        "types",
        "fields",
        "default_bib_file",
        "auto_key",
        "auto_file",
        "add_abstract",
        "add_preprint",
        "initials",
        "journal_macros",
        "protected_words",
    )
)


def _xdg_config_path():
    """The XDG standard location of `bibdeskparser.toml` (a `Path`).

    This is `$XDG_CONFIG_HOME/bibdeskparser/bibdeskparser.toml`, falling
    back to `~/.config/bibdeskparser/bibdeskparser.toml` when
    `$XDG_CONFIG_HOME` is unset.
    """
    base = os.environ.get("XDG_CONFIG_HOME") or (Path.home() / ".config")
    return Path(base) / "bibdeskparser" / CONFIG_FILENAME


def discover(bib_dir=None, config_file=None):
    """Locate the applicable `bibdeskparser.toml`, first found wins.

    The locations are searched in order of precedence:

    1. `config_file`, if given (the `Library.config.config_file`
       override); raises {exc}`FileNotFoundError` if it does not exist.
    2. `CONFIG_FILENAME` in `bib_dir` (the directory of the `.bib` file,
       or the current working directory if `bib_dir` is `None`).
    3. The file named by the `BIBDESKPARSER_CONFIG` environment
       variable, if the variable is set; raises
       {exc}`FileNotFoundError` if it does not exist. An empty value
       disables the user-level location entirely (no error).
    4. `CONFIG_FILENAME` in the XDG location (see `_xdg_config_path`),
       only when `BIBDESKPARSER_CONFIG` is unset.

    Returns the `Path` of the first existing file, or `None` if none of
    the locations has one.
    """
    if config_file is not None:
        path = Path(config_file)
        if not path.exists():
            raise FileNotFoundError(
                f"config_file does not exist: {config_file}"
            )
        return path
    local = Path(bib_dir or Path.cwd()) / CONFIG_FILENAME
    if local.exists():
        return local
    env = os.environ.get(CONFIG_ENV_VAR)
    if env is not None:
        # Unlike $XDG_CONFIG_HOME (where empty means "unset", per the
        # XDG spec), an explicitly empty value is meaningful here: no
        # user-level config at all.
        if not env:
            return None
        path = Path(env)
        if not path.exists():
            raise FileNotFoundError(f"${CONFIG_ENV_VAR} does not exist: {env}")
        return path
    xdg = _xdg_config_path()
    if xdg.exists():
        return xdg
    return None


# -- stateless parsing helpers ---------------------------------------- #


def _parse(path):
    """Parse the TOML file at `path`, raising a {exc}`ValueError` (that
    names the file) if it is malformed."""
    try:
        with open(path, "rb") as toml_file:
            return tomllib.load(toml_file)
    except tomllib.TOMLDecodeError as exc:
        raise ValueError(f"invalid config file {path}: {exc}") from exc


def _bool(raw, key):
    """Read boolean `key` from `raw`, defaulting to `True`."""
    value = raw.get(key, True)
    if not isinstance(value, bool):
        raise ValueError(f"{key} must be a boolean, not {type(value)!r}")
    return value


def _field_list(value, context):
    """Validate `value` as a list of field-name strings; return them
    lowercased."""
    if not isinstance(value, list) or not all(
        isinstance(field, str) for field in value
    ):
        raise ValueError(f"{context} must be a list of strings")
    return [field.lower() for field in value]


def _build(raw):
    """Build the entry-type/field attribute values for {class}`Config`
    from a parsed `bibdeskparser.toml` (`raw`), layering its
    customizations onto the built-in defaults."""
    unknown = set(raw) - _KNOWN_TOP_LEVEL_KEYS
    if unknown:
        warnings.warn(
            f"unknown key(s) in {CONFIG_FILENAME}: {sorted(unknown)}",
            UserWarning,
            stacklevel=4,
        )

    documented_types = {
        entry_type: {
            "required": tuple(spec["required"]),
            "optional": tuple(spec["optional"]),
        }
        for entry_type, spec in entrytypes.DOCUMENTED_TYPES.items()
    }
    recognized = set(entrytypes.RECOGNIZED_ENTRY_TYPES)
    universal = set(entrytypes.UNIVERSAL_FIELDS)
    known = set(entrytypes.KNOWN_FIELDS)

    types_table = raw.get("types", {})
    if not isinstance(types_table, dict):
        raise ValueError("[types] must be a table")
    for name, spec in types_table.items():
        if not isinstance(spec, dict):
            raise ValueError(f"[types.{name}] must be a table")
        name = name.lower()
        required = _field_list(
            spec.get("required", []), f"[types.{name}] required"
        )
        optional = _field_list(
            spec.get("optional", []), f"[types.{name}] optional"
        )
        replace = bool(spec.get("replace", False))
        if replace or name not in documented_types:
            documented_types[name] = {
                "required": tuple(required),
                "optional": tuple(optional),
            }
        else:
            base = documented_types[name]
            documented_types[name] = {
                "required": base["required"]
                + tuple(f for f in required if f not in base["required"]),
                "optional": base["optional"]
                + tuple(f for f in optional if f not in base["optional"]),
            }
        recognized.add(name)
        known.update(required)
        known.update(optional)

    fields_table = raw.get("fields", {})
    if not isinstance(fields_table, dict):
        raise ValueError("[fields] must be a table")
    extra_universal = _field_list(
        fields_table.get("universal", []), "[fields] universal"
    )
    universal.update(extra_universal)
    known.update(extra_universal)
    known.update(_field_list(fields_table.get("known", []), "[fields] known"))

    return {
        "verify_types": _bool(raw, "verify_types"),
        "verify_fields": _bool(raw, "verify_fields"),
        "documented_types": documented_types,
        "recognized_entry_types": recognized,
        "universal_fields": universal,
        "known_fields": known,
    }


def _parse_default_bib_file(raw):
    """Read the optional `default_bib_file` key from `raw`.

    Returns a `Path` with environment variables (`$VAR`) and a leading
    `~` expanded, or `None` if the key is absent.
    """
    value = raw.get("default_bib_file", None)
    if value is None:
        return None
    if not isinstance(value, str):
        raise ValueError(
            f"default_bib_file must be a string, not {type(value)!r}"
        )
    return Path(os.path.expandvars(value)).expanduser()


def _normalize_format_spec(value, context="key"):
    """Validate and normalize an auto-key or auto-file `format_spec`.

    `value` is either a single format string (applied to every entry
    type) or a per-type `dict` mapping entry-type names to format
    strings, with the empty string `""` as the fallback for
    unlisted types. Every format string is compiled (in `context`) to
    validate it, so a malformed one raises {exc}`ValueError` (or
    {exc}`NotImplementedError`, for `%i`). Returns the spec with the
    `dict` keys stripped and lowercased (a `str` is returned as is)."""
    label = "auto-file" if context == "file" else "auto-key"
    if isinstance(value, str):
        specifiers.compile_format(value, context=context)  # validate
        return value
    if isinstance(value, dict):
        if not value:
            raise ValueError(f"{label} format_spec table must not be empty")
        normalized = {}
        for entry_type, format_string in value.items():
            if not isinstance(entry_type, str):
                raise ValueError(
                    f"{label} format_spec keys must be entry-type names, "
                    f"not {type(entry_type)!r}"
                )
            if not isinstance(format_string, str):
                raise ValueError(
                    f"{label} format_spec values must be format strings, "
                    f"not {type(format_string)!r}"
                )
            specifiers.compile_format(format_string, context=context)
            normalized[entry_type.strip().lower()] = format_string
        return normalized
    raise ValueError(
        f"{label} format_spec must be a format string or a table "
        "mapping entry-type names (with '' as the fallback) to format "
        f"strings, not {type(value)!r}"
    )


class AutoKey:
    """The `[auto_key]` settings for autogenerated citation keys,
    exposed as `Library.config.auto_key`.

    Attributes:

    * `format_spec` (default `None`): the auto-key format -- a single
      format string in BibDesk's
      [format-specifier language](format-specifiers), or a per-type
      `dict` mapping entry-type names (with `""` as the fallback) to
      format strings. Assigning it validates every format string in it
      (raising {exc}`ValueError` for a malformed one) and lowercases
      the `dict` keys.
    * `lowercase` (default `False`): whether to lowercase the whole
      generated key.
    * `clean` (default `"tex"`): how much TeX markup to strip from
      field values -- one of `"none"`, `"braces"`, or `"tex"`.
    """

    def __init__(self):
        self._format_spec = None
        self.lowercase = False
        self._clean = "tex"

    @property
    def format_spec(self):
        """The auto-key format (see the class docstring)."""
        return self._format_spec

    @format_spec.setter
    def format_spec(self, value):
        self._format_spec = (
            None if value is None else _normalize_format_spec(value)
        )

    @property
    def clean(self):
        """The TeX-cleanup level (see the class docstring)."""
        return self._clean

    @clean.setter
    def clean(self, value):
        if value not in ("none", "braces", "tex"):
            raise ValueError(
                "auto_key clean must be one of 'none', 'braces', 'tex', "
                f"not {value!r}"
            )
        self._clean = value

    def __repr__(self):
        return (
            f"AutoKey(format_spec={self._format_spec!r}, "
            f"lowercase={self.lowercase!r}, clean={self._clean!r})"
        )


def _parse_auto_key(raw):
    """Read the optional `[auto_key]` table from `raw`.

    Returns an {class}`AutoKey` (holding the built-in defaults if the
    table is absent). `format_spec` and `clean` are validated by
    `AutoKey`'s own setters."""
    auto_key = AutoKey()
    table = raw.get("auto_key", None)
    if table is None:
        return auto_key
    if not isinstance(table, dict):
        raise ValueError("[auto_key] must be a table")
    unknown = set(table) - {"format_spec", "lowercase", "clean"}
    if unknown:
        warnings.warn(
            f"unknown key(s) in [auto_key]: {sorted(unknown)}",
            UserWarning,
            stacklevel=5,
        )
    if "format_spec" not in table:
        raise ValueError(
            "[auto_key] must contain a 'format_spec', e.g. "
            'format_spec = "%a1%Y%u0"'
        )
    auto_key.format_spec = table["format_spec"]
    lowercase = table.get("lowercase", False)
    if not isinstance(lowercase, bool):
        raise ValueError(
            "[auto_key] lowercase must be a boolean, "
            f"not {type(lowercase)!r}"
        )
    auto_key.lowercase = lowercase
    auto_key.clean = table.get("clean", "tex")
    return auto_key


class AutoFile:
    """The `[auto_file]` settings for autogenerated attachment file
    names, exposed as `Library.config.auto_file`.

    Attributes:

    * `format_spec` (default `None`): the auto-file format -- a single
      format string in BibDesk's
      [format-specifier language](format-specifiers) (file-name
      dialect, e.g. `"%f{Cite Key}%u0%e"`), or a per-type `dict`
      mapping entry-type names (with `""` as the fallback) to format
      strings. Assigning it validates every format string in it
      (raising {exc}`ValueError` for a malformed one, including one
      without the required `%u`/`%U`/`%n` unique specifier) and
      lowercases the `dict` keys.
    * `location` (default `Path(".")`): where auto-filed attachments
      go -- a directory path, relative to the `.bib` file's directory
      or absolute, with `~` and `$VAR` expanded on assignment. Must
      not be empty.
    * `lowercase` (default `False`): whether to lowercase the whole
      generated file name.
    * `clean` (default `"tex"`): how much TeX markup to strip from
      field values -- one of `"none"`, `"braces"`, or `"tex"`.
    * `file_automatically` (default `False`): whether
      {meth}`~bibdeskparser.Library.add_file` auto-files new
      attachments by default (the equivalent of BibDesk's *File
      papers into the papers folder automatically* preference).
    """

    def __init__(self):
        self._format_spec = None
        self._location = Path(".")
        self.lowercase = False
        self._clean = "tex"
        self.file_automatically = False

    @property
    def format_spec(self):
        """The auto-file format (see the class docstring)."""
        return self._format_spec

    @format_spec.setter
    def format_spec(self, value):
        self._format_spec = (
            None
            if value is None
            else _normalize_format_spec(value, context="file")
        )

    @property
    def location(self):
        """The auto-file location (see the class docstring)."""
        return self._location

    @location.setter
    def location(self, value):
        if not isinstance(value, (str, Path)):
            raise ValueError(
                f"auto_file location must be a path, not {type(value)!r}"
            )
        if not str(value):
            raise ValueError("auto_file location must not be empty")
        self._location = Path(os.path.expandvars(str(value))).expanduser()

    @property
    def clean(self):
        """The TeX-cleanup level (see the class docstring)."""
        return self._clean

    @clean.setter
    def clean(self, value):
        if value not in ("none", "braces", "tex"):
            raise ValueError(
                "auto_file clean must be one of 'none', 'braces', 'tex', "
                f"not {value!r}"
            )
        self._clean = value

    def __repr__(self):
        return (
            f"AutoFile(format_spec={self._format_spec!r}, "
            f"location={str(self._location)!r}, "
            f"lowercase={self.lowercase!r}, clean={self._clean!r}, "
            f"file_automatically={self.file_automatically!r})"
        )


def _parse_auto_file(raw):
    """Read the optional `[auto_file]` table from `raw`.

    Returns an {class}`AutoFile` (holding the built-in defaults if the
    table is absent). `format_spec`, `location`, and `clean` are
    validated by `AutoFile`'s own setters."""
    auto_file = AutoFile()
    table = raw.get("auto_file", None)
    if table is None:
        return auto_file
    if not isinstance(table, dict):
        raise ValueError("[auto_file] must be a table")
    known = {
        "format_spec",
        "location",
        "lowercase",
        "clean",
        "file_automatically",
    }
    unknown = set(table) - known
    if unknown:
        warnings.warn(
            f"unknown key(s) in [auto_file]: {sorted(unknown)}",
            UserWarning,
            stacklevel=5,
        )
    if "format_spec" not in table:
        raise ValueError(
            "[auto_file] must contain a 'format_spec', e.g. "
            'format_spec = "%f{Cite Key}%u0%e"'
        )
    auto_file.format_spec = table["format_spec"]
    auto_file.location = table.get("location", ".")
    for key in ("lowercase", "file_automatically"):
        value = table.get(key, False)
        if not isinstance(value, bool):
            raise ValueError(
                f"[auto_file] {key} must be a boolean, not {type(value)!r}"
            )
        setattr(auto_file, key, value)
    auto_file.clean = table.get("clean", "tex")
    return auto_file


class AddAbstract:
    """The `[add_abstract]` settings -- the defaults for the keyword
    arguments of {meth}`~bibdeskparser.Library.add_abstract` --
    exposed as `Library.config.add_abstract`.

    Attributes:

    * `min_confidence` (default `"high"`): the default for the
      `min_confidence` argument of `Library.add_abstract` (and the
      `--min-confidence` option of the `add_abstract` CLI command) --
      one of `"high"`, `"medium"`, or `"low"`.
    * `mark_empty` (default `False`): the default for the `mark_empty`
      argument (`--mark-empty/--no-mark-empty`).
    """

    def __init__(self):
        self._min_confidence = "high"
        self.mark_empty = False

    @property
    def min_confidence(self):
        """The default confidence threshold (see the class
        docstring)."""
        return self._min_confidence

    @min_confidence.setter
    def min_confidence(self, value):
        if value not in ("high", "medium", "low"):
            raise ValueError(
                "add_abstract min_confidence must be one of 'high', "
                f"'medium', 'low', not {value!r}"
            )
        self._min_confidence = value

    def __repr__(self):
        return (
            f"AddAbstract(min_confidence={self._min_confidence!r}, "
            f"mark_empty={self.mark_empty!r})"
        )


def _parse_add_abstract(raw):
    """Read the optional `[add_abstract]` table from `raw`.

    Returns an {class}`AddAbstract` (holding the built-in defaults if
    the table is absent). `min_confidence` is validated by
    `AddAbstract`'s own setter."""
    settings = AddAbstract()
    table = raw.get("add_abstract", None)
    if table is None:
        return settings
    if not isinstance(table, dict):
        raise ValueError("[add_abstract] must be a table")
    unknown = set(table) - {"min_confidence", "mark_empty"}
    if unknown:
        warnings.warn(
            f"unknown key(s) in [add_abstract]: {sorted(unknown)}",
            UserWarning,
            stacklevel=5,
        )
    settings.min_confidence = table.get("min_confidence", "high")
    mark_empty = table.get("mark_empty", False)
    if not isinstance(mark_empty, bool):
        raise ValueError(
            "[add_abstract] mark_empty must be a boolean, "
            f"not {type(mark_empty)!r}"
        )
    settings.mark_empty = mark_empty
    return settings


def _parse_bool_table(raw, name, settings, defaults):
    """Read the optional boolean-valued table `name` from `raw` into
    the freshly constructed `settings` object, whose recognized keys
    (and their built-in defaults) are given by `defaults`. Returns
    `settings`."""
    table = raw.get(name, None)
    if table is None:
        return settings
    if not isinstance(table, dict):
        raise ValueError(f"[{name}] must be a table")
    unknown = set(table) - set(defaults)
    if unknown:
        warnings.warn(
            f"unknown key(s) in [{name}]: {sorted(unknown)}",
            UserWarning,
            stacklevel=5,
        )
    for key, default in defaults.items():
        value = table.get(key, default)
        if not isinstance(value, bool):
            raise ValueError(
                f"[{name}] {key} must be a boolean, not {type(value)!r}"
            )
        setattr(settings, key, value)
    return settings


