"""BibDesk's format-specifier language.

This module is a Python translation of the parts of BibDesk's
`BDSKFormatParser` (and its supporting string utilities) that turn a
format string like `"%a1%c{journal}0%Y%u0"` into a generated string
for an {class}`~bibdeskparser.Entry`: {func}`compile_format` parses and
validates a format, and {func}`render_format` renders it. See the
[format specifiers](format-specifiers) reference page for the language
itself.

BibDesk uses this one language for both its autogenerated citation keys
and its AutoFile feature (autogenerated attachment file names).
`bibdeskparser` currently wires it up to citation-key generation only,
so the specifiers that exist solely for file names (`%l`, `%L`, `%e`,
`%E`) are rejected, exactly as BibDesk's own format validation rejects
them outside the file-name context.

.. TODO: Support the AutoFile (local-file) context, which additionally
   needs the `%l`/`%L`/`%e`/`%E` specifiers, `/` as a directory
   separator, and the "papers folder" resolution.
"""

