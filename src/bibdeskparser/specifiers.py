# A TeX command that "wraps" text, e.g. `\emph` in `\emph{word}`: a
# backslash followed by letters (or a single non-letter character),
# immediately followed by `{` and a non-`}` character. Matches
# BibDesk's `BDRangeOfTeXCommand`; `\LaTeX{}` is deliberately not
# matched (empty braces), so such commands survive.
_TEX_COMMAND_RX = re.compile(r"\\(?:[A-Za-z]+|[^A-Za-z])(?=\{[^}])")

# An unescaped curly brace (previous character is not a backslash).
_UNESCAPED_BRACE_RX = re.compile(r"(?<!\\)[{}]")

# BibDesk's virtual "Container" field (`BibItem.container`): the field
# naming a publication's venue, which differs by entry type. Types not
# listed have no container (the virtual field is empty for them).
#
# Accepting `container` as a `{Field}` name in a format (so a single
# format like `%a1%c{container}0%Y%u0` keys a mixed-type library) is a
# bibdeskparser feature that BibDesk's own format language does not
# have. It is intentionally left out of the user-facing docs: the
# documented way to key a mixed library is a per-type `format_spec`.
# The feature is supported and tested (see tests/test_specifiers.py),
# just deliberately undocumented -- do not add it to the docs.
_CONTAINER_FIELDS = {
    "article": "journal",
    "inproceedings": "booktitle",
    "conference": "booktitle",
    "incollection": "booktitle",
    "inbook": "title",
    "book": "series",
    "commented": "volumetitle",
}

_ENGLISH_MONTHS = {
    name.lower(): number
    for number, name in enumerate(calendar.month_name)
    if name
}
